package day14Conditions;

public class ForLoop {
	 static public void main(String[] args) {
		
		for(int i=1;i<11;i++) {
			System.out.println(i);
		}
		System.out.println("*************Reverse****************");
		for(int i=10;i>0;i--) {
			System.out.println(i);
		}
		int asciValue='A';
		System.out.println(asciValue);
	}
}
