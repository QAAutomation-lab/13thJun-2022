package day13Case;

public class SwitchCase2 {

	public static void main(String args[]) {
		int month = 8;
		switch (month) {
		case 1:
			System.out.println("Month is January");
			break;
		case 2:
			System.out.println("Month is February");
			break;
		case 3:
			System.out.println("Month is March");
			break;
		case 4:
			System.out.println("Month is April");
			break;
		case 5:
			System.out.println("Month is May");
			break;
		case 6:
			System.out.println("Month is Jun");
			break;
		case 7:
			System.out.println("Month is Jul");
			break;
		case 8:
			System.out.println("Month is Aug");
			break;
		case 9:
			System.out.println("Month is Sept");
			break;
		case 10:
			System.out.println("Month is Oct");
			break;
		case 11:
			System.out.println("Month is Nov");
			break;
		case 12:
			System.out.println("Month is Dec");
			break;
		default:
			System.out.println("Sorry either the value you have given is invalid or not matched with any of the case.");
			break;
		}
	}
}
